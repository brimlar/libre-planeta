<h3>About this site</h3>
Homeplaneta is a website where anyone -- a private homeowner, realtor or real estate company -- can list their property for rent
or for sale, for free.  This website was essentially born from the frustration of having to visit other real estate websites, where
information is not organized efficiently and intuitively, and where the interface is generally garish, confusing and counterintuitive.
Additionally, it's our opinion that independent people renting or selling their own property should have access to attractive-looking web listings (and Craigslist listings)
just like any "real" company out there.<br /><br />
Oh, and we are also sick of having to create a new account AND a new password for every single thing we do on the internet.  Blech.<br /><br />
We just wanted to create a site/tool where it's easy to do what you want to do -- list your property information and photographs in a simple, stylish layout, for free, with the information in an easily-viewable format on the web that you could link to other people or post on Craigslist...and then get on with your life.<br /><br />
We highly value privacy and don't have an agenda, so we won't take your e-mail address or other personal information and sell it to other people or give it away.  We wish more websites were like this.<br /><br />
We hope you have found our website and services useful.  Thank you for your time.
