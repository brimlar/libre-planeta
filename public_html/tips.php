<h4>Titles</h4>
<ul style="text-align: left;">
<li class="name"><a name="titl1">Should I give my property a title, or just use the address?</a></li>
<span class="explanation">If you can come up with some pretty, descriptive language, <b>always</b> try to give your property a title of some sort.  Titles help focus the reader
on the points you would like to emphasize, as well as help paint the picture of your property in their imagination.  <i>However</i>, if you are listing many properties on our site (or 
just don't want to spend time on it), leaving the Title field blank will move the Address field up to the title spot, meaning that your property will still have a nice, bold
heading for people to see.</span>
</ul>

<h4>Maps and Addresses</h4>
<ul style="text-align: left;">
<li class="name"><a name="maps1">How should I format my address?</a></li>
<span class="explanation">You should format your address as you would normally write it on an envelope in your country.  If this means (in the US):<br /><br />
<i>Street<br />City, State ZIP<br />USA (country not required)</i><br /><br />then write it like that.  Or, if you're from the UK, go ahead and:<br /><br />
<i>Street<br />Post Town<br />Postcode<br />UK</i><br /><br />and feel properly accomplished.  The most important thing is that Google Maps recognizes your address.</span>
<li class="name"><a name="maps2">Can I just give an intersection instead of an address?</a></li>
<span class="explanation">Yes, you can.  This is useful if you want to give an address but keep the exact address (somewhat) secret.  I recommend going to <a href="http://maps.google.com">Google Maps</a> and plugging in addresses with an intersection and seeing if they generate a valid map.  If so, go ahead and plug them into your address -- for example, as this US address:<br /><br />
<i>Street at Street<br />City, State ZIP</i><br /><br />and it should work just fine.</span>
<li class="name"><a name="maps3">Help -- the generated map does not work.</a></li>
<span class="explanation">If you are having problems with the Google Map link not showing the correct address (or not working at all), you can troubleshoot this more quickly by going to <a href="http://maps.google.com">Google Maps</a> and trying different combinations of your address in the field to see if they map correctly.  Some useful tips are:
<ul style="text-align: left; margin-top: 8px; margin-bottom: 8px;">
<li>Don't include your apartment number in the address.  Google Maps doesn't care about the apartment number, just the street address.  If you find that you have to omit the apartment number, you can always put it into the description so people know which unit it is.</li>
<li>Sometimes Google Maps is not yet available in your country -- if this is the case, then you may be out of luck with the map for now.</li>
</ul>
</span>
</ul>

<h4>Adding Additional Items as Bullet Points</h4>
<ul style="text-align: left;">
<li class="name"><a name="addl1">Adding extra bullet points in the left column</a></li>
<span class="explanation">If you have a list of other items or features concerning your property that you'd like to include in your listing in the left column (the "Statistics" column), it is very easy to do so.  When you are submitting or editing your listing, navigate to the "Other Amenities" item and put in the items you'd like to include as following:<br /><br />
<i>Item #1's description here</i><b>++</b><i>Item #2's description here</i><b>++</b><i>Item #3's description here</i><br /><br />
...and so on.  Notice that each item is separated by two plus signs (++) with no space around it.  The last item does not need the double plus signs at the end.  The double plus sign tells Homeplaneta that it should separate these items into separate bullet points, as shown in the following graphic:</span>
</ul>
