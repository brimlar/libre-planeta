<h4>Generating a PDF (coming in upcoming version)</h4>
<ul style="text-align: left;">
<li class="name"><a name="pdfg">How do I generate a free PDF of my listing?</a></li>
<span class="explanation">On your Management Page, you can create a PDF of your listing that you can then e-mail, print or distribute to others -- for free.  At your Management Page, after you've <b>Published</b> your listing and made it accessible to the outside world a new button will appear, next to the "Get CraigCode" button, called, "Generate PDF."  The picture below shows what this looks like:</span>
<img src="images/generatepdf.gif" style="border: 0px; text-align: center; margin-bottom: 20px;" />
<span class="explanation">Clicking this button will create a PDF of your current listing.  It may take a few seconds to complete, but once it's done you will be prompted to open or save the PDF to your computer.<br /><br />If you Edit your listing at some point and would like to generate a new PDF, simply click on the button again to generate a new, updated PDF.</span>
</ul>

<h4>Pictures/Photographs</h4>
<ul style="text-align: left;">
<li class="name"><a name="phot1">What are the picture size limits?</a></li>
<span class="explanation">Photographs are limited to <b>2mb file sizes</b> -- if you attempt to load a picture that is bigger than that, the upload will fail.<br /><br />
Also, as far as image dimensions are concerned, the greatest width that can be used is <b>680px</b> and the greatest height that can be shown is <b>396px</b>.  Therefore, it is in your interest
to "trim" or resize your pictures (using your own methods) to conform to these dimensions somewhat because anything greater than these dimensions is essentially wasted space, and will slow down you (and your viewers') viewing times.</span>
<li class="name"><a name="phot2">What type of pictures look best?</a></li>
<span class="explanation">Wide-format photos look best, something akin to a 16:10 or 16:9 ratio -- photos in a wide format "fill out" to the margins more attractively and really look luxurious.  
Photos in a typical 4:3 ratio (like your average 640x480 or 1024x768 ones) will leave white space on the left and right sides of the picture, space that could be used giving the viewer a richer experience.  For example, you could take a 1920x1080 photo (HD 1080, 16:9 ratio) and resize it on your own to 680x383 and it will look fantastic, fully filling out the sides and only a few pixels short of the 396px vertical limit.<br /><br />But, do what you want -- if you don't want to spend time resizing your pictures, Homeplaneta will resize them automatically to fit the dimension limits mentioned above.</span>
<li class="name"><a name="phot3">Help -- I'm getting errors trying to upload a photo.</a></li>
<span class="explanation">If you are getting errors when you are trying to upload a photo, remember that a photo must meet the following requirements:<br />
<ul style="text-align: left; margin-top: 8px; margin-bottom: 8px;">
<li>No greater than <b>2mb</b> filesize.</li>
<li>Must be in <b>.jpg</b>, <b>.png</b>, or <b>.gif</b> formats -- all other formats will not be accepted.</li>
<li>Must have a permitted extension at the end (such as <b>.jpg</b>, <b>.jpeg</b>, <b>.png</b>, or <b>.gif</b>)</li>
</ul>
</span>
</ul>

<h4>Extending the Life of Your Listing</h4>
<ul style="text-align: left;">
<li class="name"><a name="paid1">How can I extend the expiration date of my listing?</a></li>
<span class="explanation">You can extend the expiration date of your listing by purchasing a Paid Listing via PayPal on your listing's Management Page, which you can access after you first create your listing.  You have the option of the following:
<ul style='text-align: left; margin-top: 8px; margin-bottom: 8px;'>
<li>$3 - 30 days</li>
<li>$12 - 90 days</li>
<li>$28 - 180 days</li>
</ul>
The first time you select a Paid Listing, your expiration date will be pushed out the appropriate number of days from the date of purchase.  However, for Paid Listings, each time you make a purchase it will extend the life of your listing by the amount you purchase, so you can do it again and again and again, if you want.  For example, if you have a Paid Listing and your expiration date is 90 days in the future, if you purchase the $3/30 days package your new expiration date will be 120 days away.
</span>
</ul>

<h4>Posting on Craigslist</h4>
<ul style="text-align: left;">
<li class="name"><a name="crag1">How do I put my Homeplaneta listing on Craigslist?</a></li>
<span class="explanation">In order to post your listing on Craigslist, you need to 1) create a listing (it doesn't have to be a Paid Listing, it can be a free one), and 2) click the "Publish" button on your Management Page.  Once you've published your listing, a button will appear on your Management Page that says, "Get CraigCode."  Click on this button and copy the text inside the box -- this is the text you will paste into your Craigslist ad.  (We recommend right-clicking, selecting "Select All" and then right-clicking and selecting "Copy.")  That's all -- pretty easy.</span>
<li class="name"><a name="crag2">What does the generated Craigslist ad look like, and how does it differ from the one here?</a></li>
<span class="explanation">You will notice, once you create your Craigslist ad at the Craigslist website, that your ad will look very similar to the listing you created here at Homeplaneta.  However, there are two major differences:
<ul style="text-align: left; margin-top: 8px; margin-bottom: 8px;">
<li>A maximum of 8 pictures will show on the Craigslist ad.</li>
<li>Picture descriptions do not show up on the photographs.</li>
</ul>
The reason for these is to give the Craigslist reader enough information to decide whether or not it is in their interest to visit your full listing here at Homeplaneta without creating a monstrosity of a listing on Craigslist.  Note that the first 8 pictures in your listing (whatever they may be) will be the ones that are put into the Craigslist ad.  The picture sizes in the Craigslist ad are also not conducive to displaying picture descriptions at the present time (this may change in a future redesign.)
</span>

</ul>

